package org.vzw.PickALanguage.LearnTheFundamentals.Functions.Ejercicios;

/**
 * Crea un método llamado imprimirNombre que tome un parámetro nombre y lo imprima en la consola.
 */
public class EjParametrosYArgumentos {
    static void imprimirNombre(String nombre) {
        System.out.println("Nombre: " + nombre);
    }

    public static void main(String[] args) {
        imprimirNombre("Vicente");
    }
}
